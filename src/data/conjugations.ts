// Helper — look up all conjugations of a given infinitive
export function getConjugation(infinitive: string): VerbConjugation | undefined {
  return verbConjugations.find(v => v.infinitive === infinitive);
}

// Helper — get all forms (present) as quizzable pairs: [person, form]
export function getPresentPairs(infinitive: string): Array<{ person: Person; form: string }> {
  const verb = getConjugation(infinitive);
  if (!verb) return [];
  return (Object.entries(verb.present) as [Person, string][]).map(([person, form]) => ({ person, form }));
}
